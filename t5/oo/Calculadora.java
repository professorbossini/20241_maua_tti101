public class Calculadora{
 
  //isso é um método
  //tem 4 partes, pelo menos
  //tipo de retorno, nome, lista de parâmetros e corpo
  int somar(int a, int b){
    //declarar uma terceira variável chamada res valendo a soma das anteriores
    int res = a + b;
    //exibir com println
    return res;
  }

  int subtrair(int a, int b){
    return a - b;
  }

}
