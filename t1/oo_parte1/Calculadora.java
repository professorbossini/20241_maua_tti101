public class Calculadora{
  //método
  //tipo de retorno, nome, lista de parâmetros, corpo
  //há os métodos que produzem um valor
  //há os métodos que causam um efeito colateral(void)
  int somar(int a, int b){
    int resultado = a + b;
    return resultado;
  }

  //fazer a subtração
  int subtrair(int a, int b){
    return a - b;
  }
}